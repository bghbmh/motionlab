'use client'

import Link from 'next/link'
import { usePathname } from 'next/navigation'
import { useEffect, useState } from 'react'

interface Props {
	token: string
}

export default function MemberGnb({ token }: Props) {
	const pathname = usePathname()
	const [isStandalone, setIsStandalone] = useState(false)

	useEffect(() => {
		// 홈 화면에서 실행 중인지 감지
		setIsStandalone(
			window.matchMedia('(display-mode: standalone)').matches ||
			('standalone' in window.navigator && (window.navigator as any).standalone === true)
		)
	}, [])

	const tabs = [
		{
			href: `/m/${token}`,
			label: '홈',
			icon: HomeIcon,
			isActive: (p: string) => p === `/m/${token}`,
		},
		{
			href: `/m/${token}/record`,
			label: '기록',
			icon: PencilIcon,
			isActive: (p: string) => p.startsWith(`/m/${token}/record`),
		},
		{
			href: `/m/${token}/notes`,
			label: '알림장',
			icon: CalendarIcon,
			isActive: (p: string) => p.startsWith(`/m/${token}/notes`),
		},
	]

	return (
		<nav
			className="m-gnb"
			style={isStandalone ? {
				// standalone 모드: 홈 인디케이터 영역만큼 여백 추가
				paddingBottom: 'calc(env(safe-area-inset-bottom) + 30px)',
			} : undefined}
		>
			{tabs.map(({ href, label, icon: Icon, isActive }) => {
				const active = isActive(pathname)
				return (
					<Link
						key={href}
						href={href}
						className={`m-gnb-item${active ? ' m-gnb-item-active' : ''}`}
						// touch-action: manipulation — 더블탭 줌 비활성화로 300ms 딜레이 제거
						style={{ touchAction: 'manipulation' }}
					>
						<Icon active={active} />
						<span style={{ fontSize: 12, color: 'var(--m-text)' }}>{label}</span>
					</Link>
				)
			})}
		</nav>
	)
}


